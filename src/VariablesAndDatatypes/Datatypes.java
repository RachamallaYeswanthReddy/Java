package VariablesAndDatatypes;

public class Datatypes {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean b1 = true;
        System.out.println(b1);
        float price = 10.5F;
        int num= 25;
        //long, double
        short n =240;
        System.out.println(price);
        System.out.println(n);

        // 1 byte => 8 bits
//        Datatype	Memory
//        ========	=====
//        byte		1 byte
//        short		2 bytes
//        int		    4 bytes
//        long		8 bytes
//
//        float		4 bytes
//        double		8 bytes
//
//        char		2 bytes
//        boolean		NA
    }
}

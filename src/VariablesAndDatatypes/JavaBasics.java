package VariablesAndDatatypes;

public class JavaBasics {
    public static void main(String[] args) {
        System.out.print("Yesh");
        System.out.println("Yeswanth\nReddy");
//         Variables are called as Identifiers
//        Each variable has different Address
        int a = 5;
        int b = 6;
        String name = "Yeswanth Reddy";
        System.out.println(name);
        int c = 25;
        System.out.println(a+b+c);
        System.out.println(a+" "+b+" "+c);
        a = 50;
        System.out.println(a);
        a=b;
        System.out.println(a+" "+b);
    }
}

